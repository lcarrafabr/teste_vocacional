    public void getGradeVocacoes() throws ParseException{
       //Consultar Cidades
        Vocacoes oVocacao = new Vocacoes();


        if ( vocacaojTextField.getText().length() > 0 ){
            oVocacao.setVocacao(vocacaojTextField.getText()+"%");
        }

        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"#Id", "Vocações"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        gradeVocacoesjTable.setModel(modeloTabela);
        gradeVocacoesjTable.getColumnModel().getColumn(0).setPreferredWidth(50);
        gradeVocacoesjTable.getColumnModel().getColumn(1).setPreferredWidth(320);




try {
            resultSet = oVocacao.getConsultar();

            if (resultSet.getRow() == 1){
             resultSet.first();
            }


        } catch (SQLException ex) {
            Logger.getLogger(Vocacoes.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                modeloTabela.addRow(new String[]{
                    Funcoes.getCodigoFormat(resultSet.getInt("codVocacao")),
                    resultSet.getString("vocacao"),
                });

            }

        } catch (SQLException ex) {
            Logger.getLogger(Vocacoes.class.getName()).log(Level.SEVERE, null, ex);
        }

           }


     public void getFieldVocacao(int cod_vocacao) throws ParseException{

        Vocacoes oVocacao = new Vocacoes();
        ResultSet rs = null;
        oVocacao.setCodVocacao(cod_vocacao);

        try {
            rs = oVocacao.getConsultar();
            rs.next();

            ComboItem oItem = new ComboItem(rs.getInt(1), rs.getString(2));
            empresajComboBox.setSelectedItem(oItem);

            //Pegar campos
            vocacaojTextField.setText(rs.getString("vocacao"));
            empresajComboBox.setSelectedItem(rs.getString("empresa"));

        } catch (SQLException ex) {
            Logger.getLogger(Empresas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


