    private void atualizaEmpresajButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizaEmpresajButtonActionPerformed
        // TODO add your handling code here:
        try {
            Empresas oEmpresa = new Empresas();
            ResultSet rs = null;

            rs = oEmpresa.getConsultar();
            empresajComboBox.removeAllItems();
            while (rs.next()) {
                empresajComboBox.addItem(new ComboItem(rs.getInt("codEmpresa"), rs.getString("empresa")));
            }
            empresajComboBox.updateUI();
        } catch (SQLException ex) {
            Logger.getLogger(jFVocacoes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_atualizaEmpresajButtonActionPerformed

