    private void excluirjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirjButtonActionPerformed
        // TODO add your handling code here:
        Object[] options = {"Sim", "Não"};
        int sd =
                JOptionPane.showOptionDialog(null, "Deseja realmente EXCLUIR esse registro?", "Confirmação do Sistema!", JOptionPane.DEFAULT_OPTION,
                JOptionPane.WARNING_MESSAGE, null, options, options[0]);

        if (sd != 0) {
            return;
        }
        alterarjButton.setEnabled(false);
        editarjButton.setEnabled(true);
        excluirjButton.setEnabled(false);
        novojButton.setEnabled(true);

        Empresas oEmpresa = new Empresas();
        oEmpresa.setCodEmpresa(cod_empresa);

        oEmpresa.setExcluir(conexao);
        try {
            ClassConecta.con.commit();
            ClassConecta.con.setAutoCommit(true);
        } catch (SQLException ex) {
            Logger.getLogger(jFEmpresas.class.getName()).log(Level.SEVERE, null, ex);
        }

            empresajTextField.setText("");
            endereçojTextField.setText("");
            textoApresentacaojTextArea.setText("");
            textoEncerramentojTextArea.setText("");
        try {
            getGradeEmpresa();
        } catch (ParseException ex) {
            Logger.getLogger(jFEmpresas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_excluirjButtonActionPerformed

