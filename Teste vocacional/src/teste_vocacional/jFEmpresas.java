    public void getGradeEmpresa() throws ParseException{
       //Consultar Cidades
        Empresas oEmpresas = new Empresas();


        if ( empresajTextField.getText().length() > 0 ){
            oEmpresas.setEmpresa(empresajTextField.getText()+"%");
        }

        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"Id#", "empresas"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        gradeEmpresajTable.setModel(modeloTabela);
        gradeEmpresajTable.getColumnModel().getColumn(0).setPreferredWidth(50);
        gradeEmpresajTable.getColumnModel().getColumn(1).setPreferredWidth(430);




try {


            resultSet = oEmpresas.getConsultar();

            if (resultSet.getRow() == 1){
             resultSet.first();
            }


        } catch (SQLException ex) {
            Logger.getLogger(Empresas.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                modeloTabela.addRow(new String[]{
                    Funcoes.getCodigoFormat(resultSet.getInt("codEmpresa")),
                    resultSet.getString("empresa"),
                });

            }

        } catch (SQLException ex) {
            Logger.getLogger(Empresas.class.getName()).log(Level.SEVERE, null, ex);
        }

           }

    public void getFieldEmpresa(int cod_empresa) throws ParseException{

        Empresas oEmpresa = new Empresas();
        ResultSet rs = null;
        oEmpresa.setCodEmpresa(cod_empresa);

        try {
            rs = oEmpresa.getConsultar();
            rs.next();

            //Pegar campos

            empresajTextField.setText(rs.getString("empresa"));
            endereçojTextField.setText(rs.getString("endereco"));
            textoApresentacaojTextArea.setText(rs.getString("textoApresentacao"));
            textoEncerramentojTextArea.setText(rs.getString("textoEncerramento"));
        } catch (SQLException ex) {
            Logger.getLogger(Empresas.class.getName()).log(Level.SEVERE, null, ex);
        }



    }


