    private void empresajTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_empresajTextFieldKeyReleased
        // TODO add your handling code here:
        if (!alterarjButton.isEnabled()){
            try {
                Empresas oEmpresa = new Empresas();
                oEmpresa.setEmpresa(empresajTextField.getText());
                ResultSet rs = null;
                rs = oEmpresa.getConsultar();
                getGradeEmpresa();
            } catch (ParseException ex) {
                Logger.getLogger(jFEmpresas.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(jFEmpresas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if (empresajTextField.getText().equals("")){
            endereçojTextField.setText("");
            textoApresentacaojTextArea.setText("");
            textoEncerramentojTextArea.setText("");
            editarjButton.setEnabled(false);
        }
    }//GEN-LAST:event_empresajTextFieldKeyReleased

