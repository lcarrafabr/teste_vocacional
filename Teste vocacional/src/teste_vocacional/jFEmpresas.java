    private void salvarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarjButtonActionPerformed
        // TODO add your handling code here:
        salvarjButton.setEnabled(false);
        salvarMaisjButton.setEnabled(false);
        alterarjButton.setEnabled(false);
        excluirjButton.setEnabled(false);
        novojButton.setEnabled(true);
        editarjButton.setEnabled(true);

        Empresas oEmpresa = new Empresas();

        if (empresajTextField.getText().equals("")){

            JOptionPane.showMessageDialog(null, "Campo EMPRESA é obrigatório", "ATENÇÃO Mensagem do Sistema", JOptionPane.INFORMATION_MESSAGE);
            novojButtonActionPerformed(null);
        }else{

        oEmpresa.setEmpresa(empresajTextField.getText().trim().toUpperCase());
        oEmpresa.setEndereco(endereçojTextField.getText().trim().toUpperCase());
        oEmpresa.setTextoApresentacao(textoApresentacaojTextArea.getText().trim());
        oEmpresa.setTextoEncerramento(textoEncerramentojTextArea.getText().trim());
        }

        oEmpresa.setCadastrar();

        try {
            ClassConecta.con.commit();
            ClassConecta.con.setAutoCommit(true);
        } catch (SQLException ex) {
            Logger.getLogger(jFEmpresas.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            getGradeEmpresa();
        } catch (ParseException ex) {
            Logger.getLogger(jFEmpresas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_salvarjButtonActionPerformed

