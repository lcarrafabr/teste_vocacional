    private void proximaPerguntajButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_proximaPerguntajButtonActionPerformed
        try {
//                if (   sequencia == (quantPergunta + 1)   ){
//                //ocultar botões menos concluir
//                proximojButton.setVisible(false);
//                iniciarjButton.setVisible(false);
//                concluirjButton.setVisible(true);
//
//                return;
//            }else

                if ( sequencia == quantPergunta){
                proximojButton.setVisible(false);
                concluirjButton.setVisible(true);
            }


            jPanel3.removeAll();
            oGrupo = new ButtonGroup();

            // TODO add your handling code here:
            Perguntas oPergunta = new Perguntas();
            oPergunta.setSequencia(sequencia);
            rsP = oPergunta.getConsultar();
            rsP.first();
            codPergunta = rsP.getInt("codPergunta");


            perguntajLabel.setFont(perguntajLabel.getFont());
            perguntajLabel.setText(sequencia+") "+ rsP.getString("pergunta") );

            //Consultar alternativas da pergunta acima
            Alternativas oAlternativa = new Alternativas();
            oAlternativa.setCodPergunta(codPergunta);
            rsA = oAlternativa.getConsultar();
            rsA.next();
            rsA.beforeFirst();
            while (rsA.next()){
              oRadioButton = new JRadioButton( rsA.getString("letra")+" "+rsA.getString("alternativa")  ,false);
              oRadioButton.setFont(fonteAlternativajLabel.getFont());
              oRadioButton.setForeground(fonteAlternativajLabel.getForeground());
              oRadioButton.setActionCommand(null);
              oRadioButton.setActionCommand( rsA.getString("alternativa")  );
              oRadioButton.setLabel(rsA.getString("alternativa"));
              oGrupo.add(oRadioButton);

              jPanel3.setLayout( new GridLayout(7,2) );
              jPanel3.add(oRadioButton);
              jPanel3.revalidate();
              jPanel3.repaint();
            }
            sequencia++;
        } catch (SQLException ex) {
            Logger.getLogger(jFResposta.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_proximaPerguntajButtonActionPerformed

