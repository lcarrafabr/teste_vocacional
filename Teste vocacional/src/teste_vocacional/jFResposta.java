    private void iniciarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_iniciarjButtonActionPerformed
        try {
            // TODO add your handling code here:
            if ((participantejTextField.getText().equals("")) || (emailjTextField.getText().equals(""))){
            JOptionPane.showMessageDialog(null, "Por favor, confira todos os campos!","Atenção!",JOptionPane.WARNING_MESSAGE);
            return;
            }else if(ufjTextField.getText().length() > 2){
            JOptionPane.showMessageDialog(null, "O campo UF não pode ter mais de 2 (dois) caracteres","Atenção!",JOptionPane.WARNING_MESSAGE);
            return;    
            }
            else{
                
            Participantes oParticipante = new Participantes();
            oParticipante.setParticipante(participantejTextField.getText());
            oParticipante.setCidade(cidadejTextField.getText());
            oParticipante.setEmail(emailjTextField.getText());
            oParticipante.setTel(telefonejFormattedTextField.getText());
            oParticipante.setUf(ufjTextField.getText());
            oParticipante.setCadastrar();

            //Pegar quantidade de pergunta
            Perguntas oPergunta = new Perguntas();
            quantPergunta = oPergunta.getQuantidadeTotalResposta();

            codParticipante = oParticipante.getId();
            jTabbedPane1.setSelectedIndex(1);
            proximaPerguntajButtonActionPerformed(null);

            jTabbedPane1.setEnabledAt(1, true);
            jTabbedPane1.setEnabledAt(0, false);

            proximojButton.setVisible(true);
            iniciarjButton.setVisible(false);
            concluirjButton.setVisible(false);
            }

        } catch (SQLException ex) {
            iniciarjButton.setVisible(true);
            Logger.getLogger(jFResposta.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_iniciarjButtonActionPerformed

    private void concluirjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_concluirjButtonActionPerformed

        try {
            if ( sequencia == quantPergunta){
                proximojButtonActionPerformed(null);
            }
            
            jTabbedPane1.setEnabledAt(2, true);
            jTabbedPane1.setSelectedIndex(2);
            iniciarjButton.setVisible(false);
            proximaPerguntajButton.setVisible(false);
            proximojButton.setVisible(false);
            recomecarjButton.setVisible(true);
            Respostas oResposta = new Respostas();
            participantejLabel.setText(  oResposta.getTabular(codParticipante) );
            
            //Limpar variaveis publicas
            sequencia = 1;
            codPergunta = 0;
            codAlternativa = 0;
            codParticipante= 0;
            quantPergunta= 0;
            rsP = null;//tabela pergunta
            rsA = null;//tabela alternativa
            rsAlternativa = null;//tabela alternativa
            oRadioButton = null;
            oGrupo = null;
            jTabbedPane1.setEnabledAt(1, false); //Desabilita a aba 1(pergunta)
            concluirjButton.setVisible(false);
            try {
                // TODO add your handling code here:
                ClassConecta.con.commit();
                ClassConecta.con.setAutoCommit(false);
            } catch (SQLException ex) {
                Logger.getLogger(jFResposta.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (SQLException ex) {
            Logger.getLogger(jFResposta.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_concluirjButtonActionPerformed

